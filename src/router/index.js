// 挂载路由守卫
router.beforeEach((to, from, next) => {
    //to将要访问的页面
    //form从哪个页面过来
    //next是一个函数 表示放行 next('/login) 强制跳转
    if (to.path == '/login') return next();
    // const token = window.sessionStorage.getItem('token');
    // if (!token) return next('/login')
    NProgress.start()
    next();
  })
  router.afterEach(() => {
    NProgress.done()
  })
  
